<template>
    <div class="q-pa-md col-flex">
        <div
            class="q-gutter-md title-field text-dark"
        >
            <p class="q-mt-xl page-title">
                Keywords
            </p>

            <p class="question">
                What keywords describe the work?
            </p>

            <div
                v-for="(keyword, index) in keywords"
                v-bind:key="index"
            >
                <KeywordEditCard
                    v-bind:keyword="keyword"
                    v-on:update="setKeyword(index, $event)"
                    v-on:removePressed="removeKeyword(index)"
                />
            </div>
            <div
                class="errors"
                v-if="errors.keywords?.length > 0"
            >
                <p
                    class="text-negative"
                    v-for="(e, ei) in errors.keywords"
                    v-bind:key="ei"
                >
                    {{ e }}
                </p>
            </div>
            <q-btn v-on:click="addKeyword">
                Add keyword
            </q-btn>
        </div>
    </div>
    <StepperActions />
</template>

<script lang="ts">
import StepperActions from 'components/StepperActions.vue'
import KeywordEditCard from 'components/KeywordEditCard.vue'
import { defineComponent } from 'vue'
import { useCff } from '../store/cff'
import { useFileValidator } from 'src/store/validator'

export default defineComponent({
    name: 'Keywords',
    components: {
        StepperActions,
        KeywordEditCard
    },
    setup () {
        const { keywords, setKeywords } = useCff()
        const { groupedErrors } = useFileValidator()

        function addKeyword () {
            const newKeyword = ''
            const newKeywords = [...keywords.value, newKeyword]
            setKeywords(newKeywords)
        }

        function removeKeyword (index: number) {
            const newKeywords = [...keywords.value]
            newKeywords.splice(index, 1)
            setKeywords(newKeywords)
        }

        function setKeyword (index: number, newKeyword: string) {
            const newKeywords = [...keywords.value]
            newKeywords[index] = newKeyword
            setKeywords(newKeywords)
        }
        return {
            keywords,
            addKeyword,
            removeKeyword,
            setKeyword,
            errors: groupedErrors
        }
    }
})
</script>

